package myutil;

public class MyDate2 {

	int year;
	int month;
	int day;
	String weekday;

	// 코드자동생성 : alt + shift + s

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year; // 자기 자신이 갖고있는 전역변수 year를 뜻함
	}

	public int getMonth() {
		return month;
	}

	public void setMonth(int month) {
		this.month = month;
	}

	public int getDay() {
		return day;
	}

	public void setDay(int day) {
		this.day = day;
	}

	public String getWeekday() {
		return weekday;
	}

	public void setWeekday(String weekday) {
		this.weekday = weekday;
	}

}
