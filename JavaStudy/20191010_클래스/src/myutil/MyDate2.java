package myutil;

public class MyDate2 {

	int year;
	int month;
	int day;
	
	//코드자동생성: alt + shift + s
	
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getDay() {
		return day;
	}
	public void setDay(int day) {
		this.day = day;
	}
	
	
	
	
	
	
	
	
}
